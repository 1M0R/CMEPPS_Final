    private void calculoPF(){
        //PF = PFNA * FA
        //PFNA y FA los extraigo de Ventana PFA
                
        //de string a int
        labelPFNA.setText(VentanaPrincipal.PFNA+"");
        double FA = 0.65 + (VentanaPrincipal.totalCar *0.01);
        labelFA.setText(FA+"");
                       
        PF = VentanaPrincipal.PFNA * FA; 
        PF = Math.round(PF * 100.0) / 100.0;
        //de int a string
        String PF_texto = PF+"";      
        
        label_PF_Esf.setText(PF_texto);   
        label_PF_Dur.setText(PF_texto);
        
        
    }
    
