    private void label_CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_label_CalcularActionPerformed
        // TODO add your handling code here:
        double esfuerzo, duracion, elev1, elev2;
        String esf_txt, dur_txt;
                
        calculoPF();
        
        //esfuerzo = C * PF^E
        elev1 = Math.pow(PF, EE);
        esfuerzo = CE * elev1;
                
        //duracion = C * PF^E
        elev2 = Math.pow(PF, ED);
        duracion = CD * elev2;
        
        //las convierto a texto
        esf_txt = esfuerzo+"";
        dur_txt = duracion+"";
        
        label_resultado_Esfuerzo.setText(esf_txt);
        label_resultado_Duracion.setText(dur_txt);        
    }//GEN-LAST:event_label_CalcularActionPerformed

